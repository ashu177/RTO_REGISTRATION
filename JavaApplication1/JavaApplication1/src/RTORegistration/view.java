            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/mydatabase","root","password");
            String reg=t1.getText();
            String r=reg;
            PreparedStatement stm=con.prepareStatement("Select * from Registration where Reg_No=?");
            stm.setString(1,reg);
            ResultSet rs=stm.executeQuery();
            while(rs.next())
            reg=rs.getString("Card_No");
            if(reg==r)
            throw new SQLException();
            PreparedStatement st=con.prepareStatement("Select * from owner where card_no=?");
            st.setString(1,reg);
            ResultSet rs1=st.executeQuery();
            while(rs1.next())
            {
                t2.setText(rs1.getString(1));
                t3.setText(rs1.getString(2));
                t4.setText(rs1.getString(3));
                t9.setText(rs1.getString(4));
                t5.setText(rs1.getString(5));
                t6.setText(rs1.getString(6));
                t7.setText(rs1.getString(8));
                t8.setText(rs1.getString(7));
            }
